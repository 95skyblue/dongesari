package com.dongnesari.comm.admin.vo;

public class ZipVO {
	
	
	private int	   addr_id;
	private String zipcode;
	private String base_addr;
	private String plus_addr;
	private String is_using;
	public int getAddr_id() {
		return addr_id;
	}
	public void setAddr_id(int addr_id) {
		this.addr_id = addr_id;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getBase_addr() {
		return base_addr;
	}
	public void setBase_addr(String base_addr) {
		this.base_addr = base_addr;
	}
	public String getPlus_addr() {
		return plus_addr;
	}
	public void setPlus_addr(String plus_addr) {
		this.plus_addr = plus_addr;
	}
	public String getIs_using() {
		return is_using;
	}
	public void setIs_using(String is_using) {
		this.is_using = is_using;
	}
	
	
	

}
